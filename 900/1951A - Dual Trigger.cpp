int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while(t--){
        int n;
        cin >> n;
        string s;
        cin >> s;
        vector<int> v;
        int c1=0,c0=0;
        for(int i=0;i<n;i++){
            if(s[i]=='0') c0++;
            else{
                c1++;
                v.push_back(i);
            }
        }
        if(c1==0) cout << "YES\n";
        else if(c1%2!=0) cout << "NO\n";
        else if(c1==2){
            if(abs(v[0]-v[1])==1) cout << "NO\n";
            else cout << "YES\n";
        }
        else cout << "YES\n";

    }
}


