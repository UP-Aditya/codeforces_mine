int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while(t--){
        int n,a,b;
        cin >> n >> a >> b;
        string s;
        cin >> s;
        int x=0,y=0;
        int i=0;
        int f = 0;
        int g = 10000;
        while(g--){
            if(x==a && y==b){
                f = 1;
                break;
            }
            if(s[i]=='E') x++;
            else if(s[i]=='W') x--;
            else if(s[i]=='N') y++;
            else y--;
            i++;
            if(i==s.size()) i = 0;
        }
        if(f) cout << "YES\n";
        else cout << "NO\n";
    }
}


