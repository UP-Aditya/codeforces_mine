int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while(t--){
        int a,b;
        cin >> a >> b;
        if(a==b) cout << 0 << " " << 0 << '\n';
        else{
            int d = abs(a-b);
            int p = a%d;
            cout << d << " " << min(p,d-p) << '\n';
        }

    }
}


