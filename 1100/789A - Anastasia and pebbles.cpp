int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

        int n,k;
        cin >> n >> k;
        vector<int> v(n);
        for(int i=0;i<n;i++){
            cin >> v[i];
        }
        sort(v.begin(),v.end());
        reverse(v.begin(),v.end());
        int d = 0;
        int kk = k*2;
        int p = 0;
        for(int i=0;i<n;i++){
            if(v[i]>=kk){
                d += v[i]/kk;
                int r = v[i]%kk;
                if(r>k){
                    d++;
                }
                else if(r>0){
                    p++;
                }
            }
            else if(v[i]>k && v[i]<kk){
                d++;
            }
            else{
                p++;
            }
        }
        
        cout << d + (p+1)/2 << '\n';
    
}


