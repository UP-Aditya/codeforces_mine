int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while(t--){
        int n;
        cin >> n;
        vector<int> v(n);
        map<int,int> mp;
        for(int i=0;i<n;i++){
            cin >> v[i];
            mp[v[i]]++;
        }
        int df = 1e18,p = 0;
        for(auto &i : mp){
            int t = n-i.second-p;
            df = min(df,max(p,t));
            p += i.second;
        }
        cout << df << '\n';

    }
}


