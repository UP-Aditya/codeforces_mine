int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while(t--){
        int n,k;
        cin >> n >> k;
        int x = 1;
        while(x<=n){
            x *= 2;
        }
        if(x>n) x /= 2;
        int df=0;
        int p=0;
        if(!((n+1)&n)) p = n;
        while(k--){
            if(p==n)
            df += __builtin_popcountll(p);
            else df += __builtin_popcountll(x-1);
        }
        cout << df << '\n';
    }
}


