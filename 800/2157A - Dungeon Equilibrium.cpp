int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while(t--){
        int n;
        cin >> n;
        vector<int> v(n);
        for(int i=0;i<n;i++){
            cin >> v[i];
        }
        unordered_map<int,int> mp;
        for(auto &i : v){
            mp[i]++;
        }
        int x = 0;
        for(auto &i : mp){
            if(i.first > i.second){
                x += i.second;
            }
            else if(i.first < i.second){
                x += i.second-i.first;
            }
    }
    cout << x << '\n';
    }
}


