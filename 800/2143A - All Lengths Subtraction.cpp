int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while(t--){
        int n;
        cin >> n;
        vector<int> v(n);
        for(int i=0;i<n;i++){
            cin >> v[i];
        }
        int f = 0;
        for(int i=1;i<n-1;i++){
            if(v[i]<v[i-1] && v[i]<v[i+1]){
                f = 1;
                break;
            }
        }
        if(!f) cout << "YES\n";
        else cout << "NO\n";

    }
}


