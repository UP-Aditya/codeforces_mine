int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while(t--){
        int n;
        cin >> n;
        vector<int> v(n);
        for(int i=0;i<n;i++){
            cin >> v[i];
        }
        map<int,int> mp;
        for(auto &i : v){
            mp[i]++;
        }
        vector<int> f;
        for(auto &i : mp){
            f.push_back(i.second);
        }
        sort(f.begin(),f.end());
        int mx = 1;
        int p = f.size();
        for(int i=0;i<p;i++){
            mx = max(mx,((p-i)*f[i]));
        }
        cout << mx << '\n';
    }
}


