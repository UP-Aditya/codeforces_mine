int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while(t--){
        int n;
        cin >> n;
        int s = 0;
        s = (n*(n+1))/2;
        if(s%n==0){
            for(int i=1;i<=n;i++){
                cout << i << " ";
            }
            cout << '\n';
        }
        else{
            cout << 1+s%n << " ";
            for(int i=2;i<=n;i++){
                cout << i << " ";
            }
            cout << '\n';
        }
    }
}


