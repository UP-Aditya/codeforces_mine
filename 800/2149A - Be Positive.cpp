int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while(t--){
        int n;
        cin >> n;
        int c0=0,cn=0;
        vector<int> v(n);
        for(int i=0;i<n;i++){
            cin >> v[i];
            if(v[i]==0) c0++;
            if(v[i]<0) cn++;
        }
        cout << c0 + (cn%2)*2 << '\n';

    }
}


