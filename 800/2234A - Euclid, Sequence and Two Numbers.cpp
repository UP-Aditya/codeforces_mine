int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while(t--){
        int n;
        cin >> n;
        vector<int> v(n);
        for(int i=0;i<n;i++){
            cin >> v[i];
        }
        int f = 0;
        sort(v.rbegin(),v.rend());
        if(n==2){
            cout << v[0] << " " << v[1] << '\n';
            continue;
        }

        for(int i=0;i<n-2;i++){
            if(v[i]%v[i+1]!=v[i+2]){
                f = 1;
                break;
            }
        }

        if(f){
            cout << -1 << '\n';
        }
        else{
            cout << v[0] << " " << v[1] << '\n';
        }
    }
}


