int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while(t--){
        int n;
        cin >> n;
        vector<int> a(n),b(n);
        for(int i=0;i<n;i++){
            cin >> a[i];
        }
        for(int i=0;i<n;i++){
            cin >> b[i];
        }
        int p = 0;
        for(int i=n-1;i>=0;i--){
            int c = 0;
            for(int j=i;j>=0;j--){
                if(b[i]<a[j]) c++;
            }
            p = max(p,c);
        }
        cout << p << endl;
    }
}


