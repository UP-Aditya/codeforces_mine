#include<bits/stdc++.h>
using namespace std;
#define int long long
//:__: chahat

int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while(t--){
        int n,s,x;
        cin >> n >> s >> x;
        vector<int> v(n);
        for(int i=0;i<n;i++){
            cin >> v[i];
        }
        int p = accumulate(v.begin(),v.end(),0LL); 
        if(p>s) cout << "NO\n";
        else if((s-p)%x==0) cout << "YES\n";
        else cout  << "NO\n";


    }
}


