int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    set<string> drink = {
        "ABSINTH", "BEER", "BRANDY", "CHAMPAGNE", "GIN", "RUM", "SAKE", "TEQUILA", "VODKA", "WHISKEY", "WINE"
    };
    int c = 0;
    while(t--){
        string s;
        cin >> s;
        
        bool is_num = all_of(s.begin(),s.end(),::isdigit);
        if(is_num){
            int age = stoi(s);
            if(age<18) c++;
        }
        else{
            if(drink.count(s)){
                c++;
            }
        }
    }
    cout << c << '\n';
}


