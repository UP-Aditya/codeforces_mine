int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int n,k;
    cin >> n >> k;
    vector<int> a(n);
    for(int i=0;i<n;i++){
        cin >> a[i];
    }
    vector<pair<int,int>> v;
    for(int i=0;i<n;i++){
        v.push_back({a[i],i+1}); 
    }
    sort(v.begin(),v.end());

    vector<int> ans;
    for(int i=0;i<n;i++){
        if(v[i].first <= k){
            k -= v[i].first;
            ans.push_back(v[i].second);
        }
        else break;
    }
    cout << ans.size() << "\n";
    for(auto &i : ans){
        cout << i << " ";
    }
    cout << "\n";
}

