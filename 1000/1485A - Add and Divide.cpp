
#include<bits/stdc++.h>
using namespace std;
#define int long long
//:__: aditya_up62

int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while(t--){
        int a,b;
        cin >> a >> b;
        int ff = LLONG_MAX;
        for(int i=0;i<=10;i++){
            int nb = b+i;
            if(nb==1) continue;
            int temp = a;
            int c = 0;
            while(temp>0){
                temp /= nb;
                c++;
            }
            ff = min(ff,c+i);
        }
        cout << ff << '\n';
    }
}


