int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t;
    cin >> t;
    while(t--){
        int n;
        cin >> n;
        int p = 0;
        vector<int> v(n);
        for(int i=0;i<n;i++){
            cin >> v[i];
            p = gcd(p,v[i]);
        }
        if(p==1) cout << 0 << '\n';
        else if(gcd(p,n)==1){
            cout << 1 << '\n';
        }
        else if(gcd(p,n-1)==1) cout << 2 << '\n';
        else cout << 3 << '\n';
    }
}


