int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int a,b,n;
    cin >> a >> b >> n;
    string s = to_string(a);
    int f = 0;
    for(int i=0;i<=9;i++){
        int x = a*10 + i;
        if(x%b == 0){
            s += i + '0';
            f = 1;
            break;
        }
    }
    if(!f) cout << -1;
    else{
        for(int i=1;i<=n-1;i++){
            s += '0';
        }
        cout << s << '\n';
    }

}

