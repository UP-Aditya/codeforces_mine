int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t=1;
    // cin >> t;
    while(t--){
        int n,k;
        cin >> n >> k;
        vector<int> a(n),b(n);
        for(int i=0;i<n;i++){
            cin >> a[i];
        }
        for(int i=0;i<n;i++){
            cin >> b[i];
        }
        int j=0;
        auto check = [&](int mid)->bool{
            int p = k;
            if(a[j]-mid*b[j] <0) return false;
            p -= mid;
            if(p<=0) return true;
            for(int i=j+1;i<n;i++){
                p -= a[i]/b[i];
                if(p<=0) return true;
            }
            return p<=0;
        };
        vector<int> ans;
        for(int i=0;i<n;i++){
            int df = 0;
            int l = 0,h=min(k,a[i]/b[i]);
            while(l<=h){
                int mid = l + (h-l)/2;
                if(check(mid)){
                    df = mid;
                    h = mid-1;
                }
                else{
                    l = mid+1;
                }
            }
            j++;
            ans.push_back(df);
            k -= df;
    }
        for(auto &i : ans) cout << i << ' ';
    }
}


