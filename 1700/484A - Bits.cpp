#include<bits/stdc++.h>
using namespace std;
#define int long long
//:__: chahat

int32_t main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);


        int n;
        cin >> n;
        while(n--){
            int l,r;
            cin >> l >> r;
            int x = 0;
            x = (x|l);
            int mx = __builtin_popcountll(x);  // ;(
            int df = x;
            for(int i=0;i<63;i++){
                if((x&(1LL<<i))==0){
                    int p = x | (1LL << i);
                    if(p<=r){
                        x = p;
                        if(__builtin_popcountll(x)>mx){
                            mx = __builtin_popcountl(x);
                            df = x;
                        }
                    }
                }
            }
            cout << df << endl;
        }

}


